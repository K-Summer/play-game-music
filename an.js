document.addEventListener("keydown", function (event) {
    var divId;
    switch (event.key) {
        case "q":
            divId = "q";
            break;
        case "w":
            divId = "w";
            break;
        case "e":
            divId = "e";
            break;
        case "r":
            divId = "r";
            break;
        case "t":
            divId = "t";
            break;
        case "y":
            divId = "y";
            break;
        case "u":
            divId = "u";
            break;
        case "a":
            divId = "a";
            break;
        case "s":
            divId = "s";
            break;
        case "d":
            divId = "d";
            break;
        case "f":
            divId = "f";
            break;
        case "g":
            divId = "g";
            break;
        case "h":
            divId = "h";
            break;
        case "j":
            divId = "j";
            break;
        case "z":
            divId = "z";
            break;
        case "x":
            divId = "x";
            break;
        case "c":
            divId = "c";
            break;
        case "v":
            divId = "v";
            break;
        case "b":
            divId = "b";
            break;
        case "n":
            divId = "n";
            break;
        case "m":
            divId = "m";
            break;
        default:
            return;
    }
    document.getElementById(divId).style.cursor = "pointer";
    document.getElementById(divId).style.backgroundColor = "#7e3131";
    document.getElementById(divId).style.border = "2px dashed #43e571";
});

